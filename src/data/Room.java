package data;

public class Room {

    private String name;
    private int classRoom;

    public Room () {
        this.name = name;
        this.classRoom = classRoom;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNumber() {
        return classRoom;
    }

    public void setNumber(int classRoom) {
        this.classRoom = classRoom;
    }
}
